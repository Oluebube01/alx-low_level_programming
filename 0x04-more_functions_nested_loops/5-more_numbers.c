#include "main.h"

/**
 * print_more_numbers - print numbers from,0 to 14, ten times.
 * Return: Always 0.
 **/
void print_more_numbers(void)
{
	int i, j;

	for (i = 0; i <= 9; i++)
	{
		for (j = 0; j <= 14; j++)
		{
			if (j >= 10)
			{
				_putchar((j / 10) + '0');
			}
			_putchar((j % 10) + '0');
		}
		_putchar('\n');
	}
}
